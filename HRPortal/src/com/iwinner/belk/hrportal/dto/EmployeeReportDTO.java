package com.iwinner.belk.hrportal.dto;

public class EmployeeReportDTO {

	private String empID;
	private String empName;
	private Integer empReportYear;
	private String empReportName;
	private String empReportInfo;
	private String empComments;
	private String empWorkDetails;

	public String getEmpID() {
		return empID;
	}

	public void setEmpID(String empID) {
		this.empID = empID;
	}

	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public Integer getEmpReportYear() {
		return empReportYear;
	}

	public void setEmpReportYear(Integer empReportYear) {
		this.empReportYear = empReportYear;
	}

	public String getEmpReportName() {
		return empReportName;
	}

	public void setEmpReportName(String empReportName) {
		this.empReportName = empReportName;
	}

	public String getEmpReportInfo() {
		return empReportInfo;
	}

	public void setEmpReportInfo(String empReportInfo) {
		this.empReportInfo = empReportInfo;
	}

	public String getEmpComments() {
		return empComments;
	}

	public void setEmpComments(String empComments) {
		this.empComments = empComments;
	}

	public String getEmpWorkDetails() {
		return empWorkDetails;
	}

	public void setEmpWorkDetails(String empWorkDetails) {
		this.empWorkDetails = empWorkDetails;
	}

}
